    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (jTable1.getSelectedRow() >= 0) {
            this.resetButton();
            this.resetInput();

            jTextField1.setText(model.getValueAt(jTable1.getSelectedRow(), 0).toString());
            jTextField2.setText(model.getValueAt(jTable1.getSelectedRow(), 1).toString());
            jTextField3.setText(model.getValueAt(jTable1.getSelectedRow(), 3).toString());

            int i = 0;
            String find = model.getValueAt(jTable1.getSelectedRow(), 3)
                    .toString().toLowerCase();
            while (i < jComboBox2.getItemCount()) {
                // System.out.println("item ->" + jComboBox1.getItemAt(i));
                if (jComboBox2.getItemAt(i).toLowerCase().equals(find)) {
                    jComboBox2.setSelectedIndex(i);
                    break;
                }
                i++;
            }
            int j = 0;
            String temukan = model.getValueAt(jTable1.getSelectedRow(), 4)
                    .toString().toLowerCase();
            while (j < jComboBox1.getItemCount()) {
                // System.out.println("item ->" + jComboBox1.getItemAt(i));
                if (jComboBox1.getItemAt(j).toLowerCase().equals(temukan)) {
                    jComboBox1.setSelectedIndex(j);
                    break;
                }
                j++;
            }

            jButton2.setEnabled(false);
            jButton3.setEnabled(true);
            jButton4.setEnabled(true);
            jButton5.setEnabled(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nama = jTextField2.getText();
        String tanggal_lahir = jTextField3.getText();

        int gender = jComboBox2.getSelectedIndex() + 1;
        int golongan_darah = jComboBox1.getSelectedIndex() + 1;
        String id = jTextField1.getText();

        if ("".equals(nama) || "".equals(gender)) {
            JOptionPane.showMessageDialog(this,
                    "Harap lengkapi data!", "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        String query = null;
        if (!"".equals(id)) {
            query = "update pasien set"
                    + " nama = '" + nama + "',"
                    + " gender = '" + gender + "',"
                    + " tanggal_lahir = '" + tanggal_lahir + "',"
                    + " golongan_darah = '" + golongan_darah + "'"
                    + " where id = '" + id + "';";
        }

        if ("".equals(id)) {
            query = "insert into pasien values (null, '" + nama + "', '"
                    + gender + "','" + tanggal_lahir + "','" + golongan_darah + "');";
            System.out.println(query);
        }

        try {
            statement.executeUpdate(query);

            this.initInputAndButton();
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

